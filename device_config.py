class DeviceRegistry:
    """Manages device instances and their configurations."""
    
    def __init__(self, enabled_devices=None):
        """
        Args:
            enabled_devices: List of device names to enable. If None, all defaults are enabled.
        """
        self.devices = {}
        if enabled_devices is None:
            # Load all default devices by default
            enabled_devices = list(DEFAULT_DEVICES.keys())
        
        for device_name in enabled_devices:
            if device_name in DEFAULT_DEVICES:
                self.devices[device_name] = DEFAULT_DEVICES[device_name]
    
    def get_device(self, name):
        """Get a device by name."""
        return self.devices.get(name)
    
    def get_all_devices(self):
        """Get all enabled devices."""
        return self.devices
    
    def add_device(self, device):
        """Add or update a device."""
        self.devices[device.name] = device
    
    def remove_device(self, name):
        """Remove a device."""
        if name in self.devices:
            del self.devices[name]
    
    def device_names(self):
        """Get list of all device names."""
        return list(self.devices.keys())
    
    def has_device(self, name):
        """Check if device exists."""
        return name in self.devices
